import static java.lang.System.out;
public class IntegerTruncation
{
    public static void main(String[] args)
    {
        out.println("4/3 equals ");
        out.println(4/3);
        out.println(4f/3f);
        out.println(4/3f);
        out.println(4f/3);
        out.println(4/3.0);
        out.println(Math.round(6.51));
    }
}